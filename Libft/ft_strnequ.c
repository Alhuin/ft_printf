#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == 0 || s2 == 0)
		return (0);
	if (n == 0)
		return (1);
	while (*s1 == *s2)
	{
		if (n == 1 || *s1 == '\0')
			return (1);
		s1++;
		s2++;
		n--;
	}
	return (0);
}
